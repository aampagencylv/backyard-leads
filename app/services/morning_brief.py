"""
Daily morning brief — TZ-aware per-user email digest.

What lands in each user's inbox at their configured local hour:

  1. Overnight     — God Mode runs (campaign_runs since last brief)
                     + sequence sends, replies, hot leads in last 24h
  2. Today's plan  — open tasks due today + hot leads needing follow-up
                     + stuck deals (>14d in same stage)
  3. Replies awaiting action — recent email replies tagged with sentiment
  4. AI insight    — single highest-value observation, generated by
                     Sonnet from the data above (~$0.005 per user / day)
  5. Weekly stats  — sends, opens, replies, calls vs prior 7 days
  6. Footer        — manage settings + snooze + unsubscribe

The cron tick (in app/main.py lifespan) iterates active users and
calls send_brief(user, db) when local time crosses brief_hour and we
haven't sent today.
"""
