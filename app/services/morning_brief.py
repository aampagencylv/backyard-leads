import json
import logging
from dataclasses import dataclass, field
from datetime import datetime, timedelta, timezone
from typing import Optional
from zoneinfo import ZoneInfo

import httpx
from sqlalchemy import select, func
from sqlalchemy.ext.asyncio import AsyncSession

from app.config import settings
from app.models import (
    User, Company, Contact, Deal, Activity, Task, GeneratedEmail,
    Campaign, CampaignRun,
)

log = logging.getLogger("bmp.morning_brief")


# ============================================================
# Data shape
# ============================================================

@dataclass
class BriefData:
    user: dict = field(default_factory=dict)
    overnight: dict = field(default_factory=dict)
    today_priorities: dict = field(default_factory=dict)
    replies_awaiting: list = field(default_factory=list)
    weekly_stats: dict = field(default_factory=dict)
    ai_insight: str = ""


# ============================================================
# Builder — gathers the data
# ============================================================

async def build_brief(db: AsyncSession, user: User) -> BriefData:
    """Collect everything that lands in the email body. Pure data — no
    rendering, no email send. Safe to call from anywhere (preview UI,
    test endpoint, cron loop)."""
    now_utc = datetime.now(timezone.utc)
    yesterday_utc = now_utc - timedelta(days=1)
    week_ago = now_utc - timedelta(days=7)
    two_weeks_ago = now_utc - timedelta(days=14)

    brief = BriefData(user={
        "id": user.id,
        "first_name": user.first_name or "",
        "email": user.email,
    })

    # ---- Overnight (last ~24h or since last brief, whichever is shorter) ----
    since = user.last_brief_sent_at or yesterday_utc
    if since.tzinfo is None:
        since = since.replace(tzinfo=timezone.utc)
    if (now_utc - since) > timedelta(days=2):
        since = yesterday_utc  # cap at 24h on first ever brief

    overnight = {}

    # God Mode runs for campaigns this user owns
    user_campaign_ids = (await db.execute(
        select(Campaign.id).where(Campaign.created_by == user.id)
    )).scalars().all()
    if user_campaign_ids:
        runs = (await db.execute(
            select(CampaignRun)
            .where(
                CampaignRun.campaign_id.in_(user_campaign_ids),
                CampaignRun.started_at >= since,
            )
            .order_by(CampaignRun.started_at.desc())
        )).scalars().all()
        overnight["campaign_ticks"] = len(runs)
        overnight["contacts_enrolled_overnight"] = sum(r.contacts_enrolled or 0 for r in runs)
    else:
        overnight["campaign_ticks"] = 0
        overnight["contacts_enrolled_overnight"] = 0

    # Sequence sends authored by this user since the cutoff.
    # POST-CUTOVER: read from Activity (activity_type='email_sent')
    # rather than GeneratedEmail. The new engine writes Activity rows
    # on every send (via EmailChannel dual-write), and the legacy
    # send_routes path also writes Activity. So Activity is the single
    # source of truth for "emails sent" counts; querying GeneratedEmail
    # alone undercounts every new-engine send by 100%.
    sends_q = (
        select(func.count(Activity.id))
        .where(
            Activity.activity_type == "email_sent",
            Activity.created_at >= since,
        )
    )
    if user.role not in ("admin", "super_admin"):
        sends_q = sends_q.where(Activity.user_id == user.id)
    sends_count = (await db.execute(sends_q)).scalar() or 0
    overnight["sends_overnight"] = int(sends_count)

    # Replies on companies assigned to this user (or any if admin)
    replies_q = (
        select(func.count(Activity.id))
        .where(
            Activity.activity_type == "email_replied",
            Activity.created_at >= since,
        )
    )
    if user.role not in ("admin", "super_admin"):
        replies_q = replies_q.join(Company, Activity.company_id == Company.id).where(
            Company.assigned_to == user.id
        )
    overnight["replies_overnight"] = int((await db.execute(replies_q)).scalar() or 0)

    # Hot leads (lead_score >= 60) for this user
    hot_q = select(func.count(Company.id)).where(Company.lead_score >= 60)
    if user.role not in ("admin", "super_admin"):
        hot_q = hot_q.where(Company.assigned_to == user.id)
    overnight["hot_leads_count"] = int((await db.execute(hot_q)).scalar() or 0)

    brief.overnight = overnight

    # ---- Today's priorities ----
    today_start = now_utc.replace(hour=0, minute=0, second=0, microsecond=0)
    tomorrow_start = today_start + timedelta(days=1)

    tasks_today = (await db.execute(
        select(Task, Company.name)
        .join(Company, Task.company_id == Company.id)
        .where(
            Task.user_id == user.id,
            Task.completed == False,
            Task.due_date >= today_start,
            Task.due_date < tomorrow_start,
        )
        .order_by(Task.due_date)
        .limit(8)
    )).all()
    tasks = [
        {
            "id": t.id,
            "description": t.description,
            "company_id": t.company_id,
            "company_name": cname,
            "due_date": t.due_date.isoformat() if t.due_date else None,
        }
        for t, cname in tasks_today
    ]

    # Hot leads for this user, top 5 by score
    hot_q = (
        select(Company)
        .where(Company.lead_score >= 60)
        .order_by(Company.lead_score.desc())
        .limit(5)
    )
    if user.role not in ("admin", "super_admin"):
        hot_q = hot_q.where(Company.assigned_to == user.id)
    hot_rows = (await db.execute(hot_q)).scalars().all()
    hot_leads = [
        {
            "id": c.id, "name": c.name, "lead_score": c.lead_score,
            "lead_score_tier": c.lead_score_tier, "status": c.status,
        }
        for c in hot_rows
    ]

    # Stuck deals: assigned to user, not closed, no update in 14d
    stuck_q = (
        select(Deal, Company.name)
        .join(Company, Deal.company_id == Company.id)
        .where(
            Deal.assigned_to == user.id,
            Deal.stage.notin_(("closed_won", "closed_lost")),
            Deal.updated_at < two_weeks_ago,
        )
        .order_by(Deal.updated_at.asc())
        .limit(5)
    )
    stuck_rows = (await db.execute(stuck_q)).all()
    stuck_deals = [
        {
            "id": d.id, "name": d.name, "stage": d.stage,
            "company_id": d.company_id, "company_name": cname,
            "value": d.value, "days_stuck": (now_utc - _aware(d.updated_at)).days,
        }
        for d, cname in stuck_rows
    ]

    brief.today_priorities = {
        "tasks": tasks,
        "hot_leads": hot_leads,
        "stuck_deals": stuck_deals,
    }

    # ---- Replies awaiting action ----
    replies_q = (
        select(Activity, Company.name)
        .join(Company, Activity.company_id == Company.id)
        .where(
            Activity.activity_type == "email_replied",
            Activity.created_at >= since,
        )
        .order_by(Activity.created_at.desc())
        .limit(8)
    )
    if user.role not in ("admin", "super_admin"):
        replies_q = replies_q.where(Company.assigned_to == user.id)
    replies_rows = (await db.execute(replies_q)).all()
    brief.replies_awaiting = [
        {
            "id": a.id,
            "company_id": a.company_id,
            "company_name": cname,
            "reply_sentiment": a.reply_sentiment,
            "reply_sentiment_summary": a.reply_sentiment_summary,
            "preview": (a.content or "")[:160],
            "created_at": a.created_at.isoformat() if a.created_at else None,
        }
        for a, cname in replies_rows
    ]

    # ---- Weekly stats (this 7d vs prior 7d) ----
    # POST-CUTOVER: count from Activity not GeneratedEmail (see above
    # rationale on overnight sends counter).
    async def _count_user_sends(after: datetime, before: datetime) -> int:
        q = (
            select(func.count(Activity.id))
            .where(
                Activity.activity_type == "email_sent",
                Activity.created_at >= after,
                Activity.created_at < before,
            )
        )
        if user.role not in ("admin", "super_admin"):
            q = q.where(Activity.user_id == user.id)
        return int((await db.execute(q)).scalar() or 0)

    sends_this_week = await _count_user_sends(week_ago, now_utc)
    sends_prior_week = await _count_user_sends(week_ago - timedelta(days=7), week_ago)

    async def _count_user_replies(after: datetime, before: datetime) -> int:
        q = (
            select(func.count(Activity.id))
            .where(
                Activity.activity_type == "email_replied",
                Activity.created_at >= after,
                Activity.created_at < before,
            )
        )
        if user.role not in ("admin", "super_admin"):
            q = q.join(Company, Activity.company_id == Company.id).where(Company.assigned_to == user.id)
        return int((await db.execute(q)).scalar() or 0)

    replies_this_week = await _count_user_replies(week_ago, now_utc)
    replies_prior_week = await _count_user_replies(week_ago - timedelta(days=7), week_ago)

    brief.weekly_stats = {
        "sends_this_week": sends_this_week,
        "sends_prior_week": sends_prior_week,
        "replies_this_week": replies_this_week,
        "replies_prior_week": replies_prior_week,
    }

    # ---- AI insight (one Anthropic call) ----
    brief.ai_insight = await _generate_ai_insight(brief)

    return brief


def _aware(dt: Optional[datetime]) -> datetime:
    if dt is None:
        return datetime.now(timezone.utc)
    return dt if dt.tzinfo else dt.replace(tzinfo=timezone.utc)


# ============================================================
# AI insight — distill the data into ONE punchy observation
# ============================================================

INSIGHT_SYSTEM = """You are writing the single most valuable observation a sales BDR should know about their pipeline today, based on the JSON data provided.
Output ONE short sentence (under 200 chars). No preamble, no greeting.
Examples of good insights:
  - "Smith Pools opened your email 4× yesterday + visited 3 pages — call them today before they cool off."
  - "3 of 4 replies this week were 'interested' — your follow-up sequence is converting; double the daily send cap."
  - "Phoenix pool builders campaign just ran 12 ticks overnight but enrolled 0 — all targets returned dupes. Time to expand to Tucson."
  - "Sarah Martinez at Backyard Living replied 'send pricing' yesterday and there is no follow-up task on your list."
Pick the angle that matters most. If nothing notable, return an empty string."""


async def _generate_ai_insight(brief: BriefData) -> str:
    if not settings.anthropic_api_key:
        return ""
    payload = {
        "user_first_name": brief.user.get("first_name") or "",
        "overnight": brief.overnight,
        "tasks_today_count": len(brief.today_priorities.get("tasks", [])),
        "hot_leads": brief.today_priorities.get("hot_leads", []),
        "stuck_deals": brief.today_priorities.get("stuck_deals", []),
        "replies_awaiting": brief.replies_awaiting,
        "weekly_stats": brief.weekly_stats,
    }
    try:
        async with httpx.AsyncClient(timeout=15) as client:
            r = await client.post(
                "https://api.anthropic.com/v1/messages",
                headers={
                    "x-api-key": settings.anthropic_api_key,
                    "anthropic-version": "2023-06-01",
                    "content-type": "application/json",
                },
                json={
                    "model": "claude-sonnet-4-20250514",
                    "max_tokens": 120,
                    "system": INSIGHT_SYSTEM,
                    "messages": [{"role": "user", "content": json.dumps(payload, default=str)}],
                },
            )
        if r.status_code != 200:
            return ""
        data = r.json()
        text = (data.get("content", [{}])[0] or {}).get("text", "").strip()
        # Meter
        try:
            from app.services.credit_meter import meter_standalone
            await meter_standalone(action_type="ai_summary", action_ref="morning_brief",
                                   raw_cost_override_usd=0.005,
                                   metadata={"user_id": brief.user.get("id")})
        except Exception:
            pass
        return text[:280]
    except Exception as e:
        log.warning(f"AI insight failed for user {brief.user.get('id')}: {e}")
        return ""


